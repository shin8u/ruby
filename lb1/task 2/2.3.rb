def mult_cifr(n)
    num = n
    mult = 1
    while num != 0
        a = num%10
        mult *= a
        num /=10
    end
    return mult
end    

def simple(n)
    if n <=1 then
        return false
    else
        2.upto(n - 1) do |i|
            if n % i == 0 then
                return false
            end
        end
    end
    return true
end
    
def max_simple_div(n)
    max_div = 1
    2.upto(n - 1) do |div|
        if simple(div) == false and div > max_div and n % div == 0 and div % 2 != 0 then
            max_div = div
        end
    end
    return max_div
end
    
def NOD(n)
    a = mult_cifr(n)
    b = max_simple_div(n)
    if a != 0 and b != 0 then
        if a > b then
            a = a % b
        else 
            b = b % a
        end
    else
        return a + b
    end    
end    


